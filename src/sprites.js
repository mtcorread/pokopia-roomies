/**
 * Returns an <img> HTML string for a Pokemon sprite, or empty string if not found.
 * @param {string} name - Pokemon name as used in the app data
 * @param {string} [cls=''] - Optional extra CSS class(es)
 */
const BASE = import.meta.env.BASE_URL || '/';

export function spriteImg(name, cls = '') {
  const file = SPRITE_MAP[name];
  if (!file) return '';
  const c = cls ? ` ${cls}` : '';
  return `<img class="pkmn-sprite${c}" src="${BASE}sprites/${file}" alt="" loading="lazy">`;
}

/**
 * Returns the sprite URL for a Pokemon, or null if not found.
 */
export function spriteUrl(name) {
  const file = SPRITE_MAP[name];
  return file ? `${BASE}sprites/${file}` : null;
}
